class Narrowing 
{
	public static void main(String[] args) 
	{
		double d=10;
		int a=(int)d;
		System.out.println(a);

		float f=10f;
		byte b=(byte)f;

				System.out.println(b);

		int i=36;
		char c=(char)i;
		System.out.println(c);
		long l=100;
		int z=(int)l;
		System.out.println(z);
		int g=20;
		byte e=(byte)g;
		System.out.println(e);
		double h=97;
		char j=(char)h;
		System.out.println(j);
		float v=65f;
		char m=(char)v;
				System.out.println(m);


	}
}
